<h1>Allgemeine Geschäftsbedingungen (AGB) für die Nutzung der BBS Vanced Stundenplan App</h1>

<p>
	<strong>1. Vertragsgegenstand</strong>
	<br />
	Die BBS Vanced Stundenplan App (nachfolgend als "App" bezeichnet). Diese AGB regeln die Bedingungen
	der Nutzung der App, einschließlich der kostenlosen und PRO-Version.
</p>

<p>
	<strong>2. Einstellung des Services</strong>
	<br />
	Der Anbieter behält sich das Recht vor, den Service der App jederzeit einzustellen, ohne dass dies
	zu Ansprüchen der Nutzer führt. In einem solchen Fall wird der Anbieter angemessene Maßnahmen ergreifen,
	um die Nutzer im Voraus über die Einstellung des Services zu informieren.
</p>

<p>
	<strong>3. Haftungsausschluss für Informationen</strong>
	<br />
	Der Anbieter übernimmt keine Haftung für die Richtigkeit, Vollständigkeit oder Aktualität der in der
	App bereitgestellten Informationen, insbesondere bezüglich Stundenplänen und Veranstaltungen. Die Verantwortung
	für die Überprüfung und Aktualisierung der Stundenplandaten liegt beim Nutzer. Dies gilt insbesondere
	für die Verwendung des Abinotenrechners.
</p>

<p>
	<strong>4. Keine Uptime-Garantien</strong>
	<br />
	Der Anbieter gewährleistet keine ununterbrochene Verfügbarkeit (Uptime) der App. Es besteht keine Garantie
	für die ständige Erreichbarkeit der App, und der Anbieter haftet nicht für eventuelle Ausfallzeiten
	oder Unterbrechungen des Services.
</p>

<p>
	<strong>5. PRO-Version</strong>
	<br />
	Die PRO-Version der App bietet zusätzliche Funktionen gegen eine Gebühr. Auch hier gibt es keine Uptime
	Garantie und der Service kann jederzeit eingestellt werden.
</p>

<p>
	<strong>6. Änderungen der AGB</strong>
	<br />
	Der Anbieter behält sich das Recht vor, diese AGB jederzeit zu ändern.
</p>
